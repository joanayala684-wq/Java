package control;

public class Propietario {
	private String dni, NomAp, apellido;

	public Propietario(String dni, String NomAp, String apellido) {
		super();
		this.dni = dni;
		this.NomAp = NomAp;
		this.apellido = apellido;
	}

	public String getDni() {
		return dni;
	}

	public String getNomAp() {
		
		return 	NomAp;
	}



}
