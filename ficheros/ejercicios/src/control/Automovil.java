package control;

public class Automovil {
private String matricula, fecha_compra, dni;
private double precio;

public Automovil(String matricula, String fecha_compra, String dni, double precio) {
	super();
	this.matricula = matricula;
	this.fecha_compra = fecha_compra;
	this.dni = dni;
	this.precio = precio;
}
}
